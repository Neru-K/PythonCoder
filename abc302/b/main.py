H, W = map(int,input().split())
S = [input() for _ in range(H)]

# 上下左右への移動を表す配列
dx = [0, 1, 0, -1]
dy = [1, 0, -1, 0]

# ここで、(y, x)は現在位置を表します
y, x = 0, 0  # 例えば、グリッドの開始位置など

# 上下左右の移動をループで処理
for i in range(4):
    nx = x + dx[i]
    ny = y + dy[i]
    # ここで、nxとnyは移動先の座標を表します
    # 移動先が迷路の範囲内であるか、壁でないかなどのチェックを行う
    if 0 <= nx < グリッドの幅 and 0 <= ny < グリッドの高さ and 迷路[ny][nx] != 壁を表す値:
        # 移動可能な場合の処理をここに書く
        print("移動先:", nx, ny)
